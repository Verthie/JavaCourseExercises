public class Decimals {
    public static void main(String[] args) {
        double percentage = 73.5;
        System.out.println(percentage + " of percentages are made up");

        double wallet = 200000000000000D; // always use double for math calculations. This will be stored as double:
                            // 20.000
        int people = 3;
        System.out.println(wallet / people);
    }
}
